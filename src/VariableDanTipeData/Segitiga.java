package VariableDanTipeData;

public class Segitiga {
    public static void main(String[] args) {
        System.out.println("Menghitung Luas Segitiga");
        int a = 20; // Alas
        int t = 16; // Tinggi
        double l = 0.5;
        double Luas = l * a * t;
        int i =(int) Luas;
        System.out.println("Luas dari segetiga tersebut adalah: "+ i+"cm");
    }
}

